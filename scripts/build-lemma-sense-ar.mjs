function cleanEn(s) {
  return String(s || "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, " ");
}

function senseFromEnglish(meaning) {
  const key = cleanEn(meaning);
  if (!key) return null;
  if (EN_GLOSS_AR[key]) return EN_GLOSS_AR[key];

  // Strip outer parentheses markers like "(of) allah"
  const stripped = key.replace(/[()]/g, "").replace(/\s+/g, " ").trim();
  if (EN_GLOSS_AR[stripped]) return EN_GLOSS_AR[stripped];

  // "the X" → try X
  if (stripped.startsWith("the ")) {
    const rest = stripped.slice(4);
    if (EN_GLOSS_AR[rest]) return EN_GLOSS_AR[rest];
    if (EN_GLOSS_AR[`the ${rest}`]) return EN_GLOSS_AR[`the ${rest}`];
  }

  return null;
}

function normalizeLemmaKey(lemma) {
  return String(lemma || "").trim();
}

async function main() {
  const irabDir = path.join(root, "data", "irab");
  const surahDir = path.join(root, "data", "surahs");
  const files = (await readdir(irabDir)).filter((f) => f.endsWith(".json"));

  /** @type {Map<string, Map<string, number>>} */
  const lemmaEnVotes = new Map();
  /** @type {Map<string, number>} */
  const lemmaCount = new Map();

  for (const file of files) {
    const irab = JSON.parse(await readFile(path.join(irabDir, file), "utf8"));
    const surah = JSON.parse(
      await readFile(path.join(surahDir, file), "utf8"),
    );

    const wordMeaning = new Map();
    for (const verse of surah.verses ?? []) {
      for (const w of verse.words ?? []) {
        if (w.charType && w.charType !== "word") continue;
        wordMeaning.set(`${verse.verseNumber}:${w.position}`, w.meaning || "");
      }
    }

    for (const verse of irab.verses ?? []) {
      for (const w of verse.words ?? []) {
        const lemma = normalizeLemmaKey(w.lemma);
        if (!lemma) continue;
        lemmaCount.set(lemma, (lemmaCount.get(lemma) || 0) + 1);
        const en = wordMeaning.get(`${verse.verseNumber}:${w.position}`) || "";
        if (!en) continue;
        if (!lemmaEnVotes.has(lemma)) lemmaEnVotes.set(lemma, new Map());
        const votes = lemmaEnVotes.get(lemma);
        const k = cleanEn(en);
        votes.set(k, (votes.get(k) || 0) + 1);
      }
    }
  }

  /** @type {Record<string, { sense: string; source: string; count: number }>} */
  const senses = {};

  for (const [lemma, count] of lemmaCount) {
    if (LEMMA_SENSE[lemma]) {
      senses[lemma] = {
        sense: LEMMA_SENSE[lemma],
        source: "arabya-curated",
        count,
      };
      continue;
    }

    const votes = lemmaEnVotes.get(lemma);
    if (!votes || !votes.size) continue;
    const topEn = [...votes.entries()].sort((a, b) => b[1] - a[1])[0][0];
    const fromEn = senseFromEnglish(topEn);
    if (fromEn) {
      senses[lemma] = {
        sense: fromEn,
        source: "arabya-en-paraphrase",
        count,
      };
    }
  }

  const payload = {
    version: 1,
    license:
      "Arabya original Arabic study senses. Lemma keys from Quranic Arabic Corpus (GPL). Not a third-party Arabic WBW redistribution.",
    attribution: {
      morphology: "http://corpus.quran.com",
      senses: "Arabya",
    },
    lemmaCount: Object.keys(senses).length,
    senses,
  };

  await writeFile(outPath, JSON.stringify(payload, null, 2), "utf8");
  console.log(
    `Wrote ${payload.lemmaCount} lemma senses → data/sources/lemma-sense-ar.json`,
  );
  const curated = Object.values(senses).filter(
    (s) => s.source === "arabya-curated",
  ).length;
  const para = Object.values(senses).filter(
    (s) => s.source === "arabya-en-paraphrase",
  ).length;
  console.log(`  curated=${curated} en-paraphrase=${para}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
